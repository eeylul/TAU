    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                String s1	= jTextField2.getText();
		String s2	= jTextField3.getText();
		String s3	= jTextField1.getText();
		double dep	= Double.parseDouble(s1);
		double rate	= Double.parseDouble(s2);
                
		int	 time	= Integer.parseInt(s3);

		double q = 1 + rate / 100;
		double finalAmount = dep * Math.pow(q, time);
		finalAmount = Math.rint(finalAmount * 100);
		finalAmount = finalAmount / 100;

		jLabel4.setText("Das Endkapital wäre " + finalAmount);
    }//GEN-LAST:event_jButton1ActionPerformed
